# This script runs proguard on the Flume jars generated by the flume-ng-dist
# module.

ROOT=$(cd $(dirname $0)/..; pwd)

IN_CONF=$ROOT/src/main/resources/flume/proguard.conf
OUT_CONF=$ROOT/target/proguard.conf
> $OUT_CONF
FLUME_JARS=$(find $ROOT/target/apache-flume-1.7.0-SNAPSHOT-bin/|grep jar | egrep '(lib|tools)/flume.*jar')
for JAR in $FLUME_JARS; do
  echo "-injars $JAR" >> $OUT_CONF
done
LIB_JARS=$(find $ROOT/target/apache-flume-1.7.0-SNAPSHOT-bin/|grep jar | egrep -v '(lib|tools)/flume.*jar')
for JAR in $LIB_JARS; do
  echo "-libraryjars $JAR" >> $OUT_CONF
done
cat $IN_CONF >> $OUT_CONF

set -x
proguard.sh @$OUT_CONF
